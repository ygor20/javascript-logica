//Vetores ou array

let notas =
 [7.5,6.3,4.2,9.5];

 console.log("Notas iniciais", notas);

 //Ordenar em ordem crescente

 notas.sort ((a, b) => a - b);

 console.log ("Notas Ordenadas(crescente):", notas);

 //Ordenar em ordem decrescente
 notas.sort((a,b) => b - a);

 console.log("Notas ordenadas (decrescente:", notas);

